/**
  * @brief This function handles TIM2 global interrupt.
  */
void TIM2_IRQHandler(void)
{
  /* USER CODE BEGIN TIM2_IRQn 0 */
	static uint32_t u32_counter = 0;

  /* USER CODE END TIM2_IRQn 0 */
  HAL_TIM_IRQHandler(&htim2);
  /* USER CODE BEGIN TIM2_IRQn 1 */
#if 1
	if(u32_counter < u32_pulse_number)
	{
		u32_counter ++;
	}
	else
	{
		// Switch off interrupt here and everything else in last interrupt of TIM3
		__HAL_TIM_DISABLE_IT(&htim2, TIM_IT_CC1);
		u32_counter = 0;
	}
#endif
  /* USER CODE END TIM2_IRQn 1 */
}

/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */
    static uint32_t u32_counter = 0;

    uint32_t u32_current_number = 0;

  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */
#if 1
    // Set current pulse:
    board_get_uint32_state(PULSE_NUMBER_IN_BANCH, &u32_current_number);
    u32_current_number++;
    board_set_uint32_state(PULSE_NUMBER_IN_BANCH, u32_current_number);

    if(u32_counter < u32_pulse_number)
	{
		u32_counter ++;
	}
	else
	{
		/* Disable the Capture compare channel TIM2: */
		TIM_CCxChannelCmd(htim2.Instance, TIM_CHANNEL_1, TIM_CCx_DISABLE);
		/* Disable TIM2: */
		__HAL_TIM_DISABLE(&htim2);

		/* Stop and disable TIM3: */
		HAL_TIM_PWM_Stop_IT(&htim3, TIM_CHANNEL_3);
		u32_counter = 0;

		HAL_GPIO_WritePin(GPIOF, GPIO_PIN_6, GPIO_PIN_RESET);
	}
#endif
  /* USER CODE END TIM3_IRQn 1 */
}

